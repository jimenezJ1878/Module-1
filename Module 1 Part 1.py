# Python program to add and subtract two numbers

# Getting input from the user
num1 = float(input("Enter first number (num1): "))
num2 = float(input("Enter second number (num2): "))

# Adding the numbers
addition = num1 + num2

# Subtracting the numbers
subtraction = num1 - num2

# Displaying the results
print("The addition of", num1, "and", num2, "is:", addition)
print("The subtraction of", num1, "from", num2, "is:", subtraction)
